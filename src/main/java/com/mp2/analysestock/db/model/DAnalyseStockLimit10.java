    /**
     * 保留小数6位
     *
     * @param val
     * @return
     */
    private double setScale(double val) {
        BigDecimal re = new BigDecimal(val);
        return re.setScale(5, BigDecimal.ROUND_HALF_UP).doubleValue();
    }

    /**
     * 获取制定市场的无效过滤比例值
     *
     * @return
     */
    private double getFilterRate_sh(int stockCount) {
        return (float) stockCount / 300;
    }
    private double getFilterRate_sz(int stockCount) {
        return (float) stockCount / 300;
    }

    // 上海 100-6100

    public double getSh_lup10_rate() {
        if (getSh_stock_count() == 0
                || (float) getSh_lup10_count() <= getFilterRate_sh(getSh_stock_count())) {
            return 100;
        } else{
            return setScale((float) getSh_lup10_count() / (float) getSh_stock_count() * 6000 + 100);
        }
    }

